void PrintImage(int [,] image) // вывод массива на печать
{
    for ( int i = 0; i < image.GetLength(0); i++)
{
    for ( int j = 0; j < image.GetLength(1); j++)
    {
        // Console.Write($"{matrix[i, j] }");
        if (image[i,j] == 0) Console.Write($" ");
        else Console.Write($"+");        
    }
    Console.WriteLine();
}
}
void FillImage(int i, int j) // использовали рекурсию
{
    if (pic[i,j]==0)
    {
        pic[i,j] = 1;
        FillImage(i-1,j);
        FillImage(i,j-1);
        FillImage(i+1,j);
        FillImage(i,j+1);
    }
}
